bool debug1_read_responder_public_certificate_chain(
    uint32_t base_hash_algo, uint32_t base_asym_algo, void **data,
    size_t *size, void **hash, size_t *hash_size)
{
    bool res;
    void *file_data;
    size_t file_size = (size_t) sizeof(cert_chain_hardcoded);
    spdm_cert_chain_t *cert_chain;
    size_t cert_chain_size;
    char *file;
    const uint8_t *root_cert;
    size_t root_cert_len;
    size_t digest_size;
    bool is_requester_cert;
    bool is_device_cert_model;
        
    /* Somente para teste */
    
    const uint8_t *root_cert_buffer;
    size_t root_cert_buffer_size;
    const uint8_t *leaf_cert_buffer;
    size_t leaf_cert_buffer_size;
    /* teste */
    is_requester_cert = false;

    /*default is true*/
    is_device_cert_model = true;

    *data = NULL;
    *size = 0;
    if (hash != NULL) {
        *hash = NULL;
    }
    if (hash_size != NULL) {
        *hash_size = 0;
    }

    if (base_asym_algo == 0) {
        return false;
    }

    digest_size = libspdm_get_hash_size(base_hash_algo);

    cert_chain_size = sizeof(spdm_cert_chain_t) + digest_size + file_size;
    cert_chain = (void *)malloc(cert_chain_size);
    if (cert_chain == NULL) {
        return false;
    }
    cert_chain->length = (uint16_t)cert_chain_size;
    cert_chain->reserved = 0;

    res = libspdm_verify_cert_chain_data(cert_chain_hardcoded, file_size,
                                         base_asym_algo, base_hash_algo,
                                         is_device_cert_model);
    if (!res) {
        if (!libspdm_x509_get_cert_from_cert_chain(
            cert_chain_hardcoded, file_size, 0, &root_cert_buffer,
            &root_cert_buffer_size)) {
        
        printf("VerifyCertificateChainData - FAIL (get root certificate failed) \n");}
        
        
        
         if (!libspdm_x509_verify_cert_chain(root_cert_buffer, root_cert_buffer_size,
                                        cert_chain_hardcoded, file_size)){    
            
            
             printf("VerifyCertificateChainData - FAIL (cert chain verify failed)!!!\n");}
            
         if (!libspdm_x509_get_cert_from_cert_chain(
            cert_chain_coded, file_size, -1,
            &leaf_cert_buffer, &leaf_cert_buffer_size)){
            
            printf("VerifyCertificateChainData - FAIL (get leaf certificate failed)!!!\n");
            
            }    
        
         if (!libspdm_x509_certificate_check(leaf_cert_buffer, leaf_cert_buffer_size,
                                        base_asym_algo, base_hash_algo,
                                        is_device_cert_model)){
                                        
                                        printf("VerifyCertificateChainData - FAIL (leaf certificate check failed)!!!\n");
                                        
                                        }
            
        printf("Certificado com problema 1! \n");
    }


    /* Get Root Certificate and calculate hash value*/

    res = libspdm_x509_get_cert_from_cert_chain(cert_chain_hardcoded, file_size, 0, &root_cert,
                                                &root_cert_len);
    if (!res) {
        free(cert_chain);
        printf("Certificado com problema 2! \n");
        return false;
    }

    res = libspdm_hash_all(base_hash_algo, root_cert, root_cert_len,
                           (uint8_t *)(cert_chain + 1));
    if (!res) {
        free(cert_chain);
        printf("Hash Error! \n");
        return false;
    }
    
    libspdm_copy_mem((uint8_t *)cert_chain + sizeof(spdm_cert_chain_t) + digest_size,
                     cert_chain_size - (sizeof(spdm_cert_chain_t) + digest_size),
                     cert_chain_hardcoded, file_size);

    *data = cert_chain;
    *size = cert_chain_size;
    
    if (hash != NULL) {
        *hash = (cert_chain + 1);
    }
    if (hash_size != NULL) {
        *hash_size = digest_size;
    }
    
    for(int i = 0; i<8; i++){
    	printf("HARD_HASH - 0x%x\n", hard_hash[i]);
    }
    	
    for(int i = 0; i<64; i++)
        printf("CERT_CHAIN - 0x%x\n", cert_chain[i]);

    return true;
}

